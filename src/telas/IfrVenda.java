    @Override
    public void definirValor(String[] valores, String itemPesquisa) {
        if (itemPesquisa.equalsIgnoreCase("cliente")) {
            txtIdCliente.setText(valores[0]);
            txtNomeCliente.setText(valores[1]);
        } else if (itemPesquisa.equalsIgnoreCase("colaborador")) {
            txtIdColaborador.setText(valores[0]);
            txtNomeColaborador.setText(valores[1]);
        } else if (itemPesquisa.equalsIgnoreCase("produto")) {
            txtIdProduto.setText(valores[0]);
            txtProduto.setText(valores[1]);
            txtValor.setText(valores[2]);
        }
    }

