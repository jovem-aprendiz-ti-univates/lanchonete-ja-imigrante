    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        Venda venda = new Venda();

        venda.setData(tffDataVenda.getText());
        venda.setClientes_id(Integer.parseInt(txtIdCliente.getText()));
        venda.setColaboradores_id(Integer.parseInt(txtIdColaborador.getText()));

        String retorno = new VendaDAO().salvar(venda);

        System.out.println("Id Venda retorno: " + retorno);

        if (!retorno.contains("ERROR")) {
            idVenda = Integer.parseInt(retorno);

            btnCriar.setEnabled(false);
            btnLocalizarCliente.setEnabled(false);
            btnLocalizarColaborador.setEnabled(false);
            btnLocalizarProduto.setEnabled(true);
            btnRemover.setEnabled(true);
            btnInserir.setEnabled(true);

        } else {
            JOptionPane.showMessageDialog(null, "Erro ao criar Venda: " + retorno);
        }
    }//GEN-LAST:event_btnCriarActionPerformed

    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        VendaProduto vendaProduto = new VendaProduto();

        vendaProduto.setVendas_id(idVenda);
        vendaProduto.setProdutos_id(Integer.parseInt(txtIdProduto.getText()));
        vendaProduto.setQuantidade(Double.parseDouble(txtQtde.getText()));
        vendaProduto.setPreco(Double.parseDouble(txtValor.getText()));

        if (new VendaProdutoDAO().salvar(vendaProduto) == null) {
            JOptionPane.showMessageDialog(null, "Item inserido.");

            new VendaProdutoDAO().popularTabela(tblVendaProduto, "", idVenda);

            total += (vendaProduto.getPreco() * vendaProduto.getQuantidade());

            txtTotal.setText(Formatacao.formatarDecimal(total));

            txtIdProduto.setText("");
            txtProduto.setText("");
            txtValor.setText("");
            txtQtde.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Erro na inserção.");
        }
    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (tblVendaProduto.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um registro.");
        } else {
            int idVendaInteger = Integer.parseInt(String.valueOf(tblVendaProduto.getValueAt(tblVendaProduto.getSelectedRow(), 0)));
            int idProdutoInteger = Integer.parseInt(String.valueOf(tblVendaProduto.getValueAt(tblVendaProduto.getSelectedRow(), 1)));

            System.out.println("VendaProdutoInt: " + idVendaInteger);

            String retorno = new VendaProdutoDAO().excluir(idVendaInteger, idProdutoInteger);

            if (retorno == null) {
                JOptionPane.showMessageDialog(null, "Registro removido.");

                double quantidade = Double.parseDouble(String.valueOf(tblVendaProduto.getValueAt(tblVendaProduto.getSelectedRow(), 3)));
                double preco = Double.parseDouble(String.valueOf(tblVendaProduto.getValueAt(tblVendaProduto.getSelectedRow(), 4)));

                total -= (quantidade * preco);

                txtTotal.setText(Formatacao.formatarDecimal(total));

                new VendaProdutoDAO().popularTabela(tblVendaProduto, "", idVendaInteger);
            } else {
                JOptionPane.showMessageDialog(null, "Erro: Problemas ao remover registro.\n\n" + retorno);
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

